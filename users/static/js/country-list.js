document.getElementById('switzerland').onmouseover = function(){
    document.getElementById('switzerland-text').style.opacity = 1
    document.getElementById('switzerland-text').style.transition = "1s"
    document.getElementById('switzerland-text').style.backgroundSize = "100% 2px"
}
document.getElementById('switzerland').onmouseout = function(){
    document.getElementById('switzerland-text').style.opacity = "0%"
    document.getElementById('switzerland-text').style.backgroundSize = ""
}


